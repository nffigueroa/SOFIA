    private void cargarTable() 
    {
        //Metodo para llenar la tabla producto con los parametros, nombre de columnas y columnas que quiero eliminar
           BL.Funciones_frm_medicion medicion= new Funciones_frm_medicion();
        consultarDatosUsuario();
        try{
            parametrosTabla();
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }  
        try{
        tbl_medicion.setModel(medicion.llenarMedicion(usuario_activo.getId_sucursal(),columnas, ancho_columnas));
        }
        catch(Exception ex) 
        {
            ex.printStackTrace();
        }
         
    }
    
     private void btn_agregar_categoria()
    {
       consultarDatosUsuario();
       try
       {
       Funciones_frm_categoria categoria = new Funciones_frm_categoria();
       categoria.InsertarCategoria(txt_medicion.getText(),usuario_activo.getId_usuario());
       cargarTable();
       JOptionPane.showMessageDialog(null, "Se Registro Exitosamente!");
  
       }
       catch(Exception ex)
       {
           JOptionPane.showMessageDialog(null, ex);
       }

       
    }

